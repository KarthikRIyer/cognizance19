package com.mdgiitr.karthik.cognizance19.models;

public class WorkshopsModel {
    String imageURL, workshopName, workshopCategory, workshopFollowers;

    public WorkshopsModel() {
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public String getWorkshopName() {
        return workshopName;
    }

    public void setWorkshopName(String workshopName) {
        this.workshopName = workshopName;
    }

    public String getWorkshopCategory() {
        return workshopCategory;
    }

    public void setWorkshopCategory(String workshopCategory) {
        this.workshopCategory = workshopCategory;
    }

    public String getWorkshopFollowers() {
        return workshopFollowers;
    }

    public void setWorkshopFollowers(String workshopFollowers) {
        this.workshopFollowers = workshopFollowers;
    }
}
